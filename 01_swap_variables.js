var my_number = "100";
var your_number = "200";
var temp = my_number // temporary vaiable to store my_number

console.log("before swap");
console.log(my_number, your_number);

my_number = your_number;
console.log("value override");
console.log(my_number, your_number); // override my_number with your_number


my_number = your_number
your_number = temp

console.log("after swap");
console.log(my_number, your_number)

